			Cat::Cat() {
	setType("Cat");
	std::cout << "A new stray " + getType() + " has born!\n";
}

			Cat::Cat(const Cat& other) {
	*this = other;
}

Cat&		Cat::operator=(const Cat& other) {
	if (this != &other) {
		setType((other.getType()));
	}
	return *this;
}

			Cat::~Cat() {
	std::cout << "A stray " + getType() + " has died!\n";
}

