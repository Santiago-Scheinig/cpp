#include "Point.hpp"
/*--------------------------------------------------------------------------*/
/*--------------------------ORTHODOX CANONICAL FORM-------------------------*/
/*--------------------------------------------------------------------------*/

		Point::Point() 
			 :	x(Fixed()),
				y(Fixed()) {}

		Point::Point(const float x, const float y)
			 :	x(Fixed(x)),
			 	y(Fixed(y)) {}

		Point::Point(const Point& other)
			 :	x(other.x),
				y(other.y) {}

Point&	Point::operator=(const Point& other) {
	(void) other;
	return (*this);
}

		Point::~Point() {
}

/*--------------------------------------------------------------------------*/
/*----------------------------------PUBLIC----------------------------------*/
/*--------------------------------------------------------------------------*/

Fixed	Point::getX() const {
	return (this->x);
}

Fixed	Point::getY() const {
	return (this->y);
}

Fixed	Point::crossProduct(Point A, Point B, Point P) {
	Fixed	result;

	result = (B.getX() - A.getX()) * (P.getY() - A.getY()) - (B.getY() - A.getY()) * (P.getX() - A.getX());
	return (result);
}

